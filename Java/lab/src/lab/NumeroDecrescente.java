package lab;

import java.util.Scanner;

public class NumeroDecrescente {
	public static void main(String[] args) {
		
		int n1;
		Scanner ent = new Scanner(System.in);
		
		System.out.println("Qual número vai decrescer?");
		n1 = ent.nextInt();
		
		while(n1>0) {
			System.out.println(n1);
			n1--;
		}
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
}
